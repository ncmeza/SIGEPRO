     public void cargarListaProyectos(ArrayList<String[]> lista){
          DefaultTableModel tabla = new DefaultTableModel();
          tabla.addColumn("Id Proyecto");
          tabla.addColumn("Descipcion del Proyecto");
          tabla.addColumn("Avance Promedio");
          tabla.addColumn("Costo del Proyecto");
          tabla.addColumn("Responsable del Proyecto");
          tabla.addColumn("ID Cliente");
          for(String[] fila: lista){
              tabla.addRow(fila);
          }
          jTable2.setModel(tabla);
          
      }

